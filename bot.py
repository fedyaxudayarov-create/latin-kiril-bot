    if st.get("awaiting_search"):
        q = (m.text or "").strip()
        set_state(m.chat.id, awaiting_search=False)
        mode = st.get("mode")

        if mode == "mahalla_search":
            set_state(m.chat.id, mode="mahalla", mfy_query=q, mfy_page=0)
            kb, _, _ = mahalla_buttons_list(page=0, query=q)

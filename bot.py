def normalize_phone(p: str) -> str:
    p = (p or "").strip()
    if not p:
        return ""
    # +998(93)064-26-38 -> +998930642638
    digits = re.sub(r"[^\d+]", "", p)
    # ba'zida 998... keladi
    if digits.startswith("998") and not digits.startswith("+998"):
        digits = "+" + digits
    if digits.startswith("+998") and len(digits) < 13:
        # qolsa - qanday bo‘lsa ham qaytaramiz
        return digits
    return digits


    nav = []
    if page > 0:
        nav.append(types.InlineKeyboardButton("â¬…ï¸ Oldingi", callback_data=f"mfy:p:{page-1}"))
    if page < max_page:
        nav.append(types.InlineKeyboardButton("Keyingi âž¡ï¸", callback_data=f"mfy:p:{page+1}"))

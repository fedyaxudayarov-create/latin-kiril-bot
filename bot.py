def short_role(role: str) -> str:
    r = tr(role or "")
    for k, v in [("rahbari","rahb."),("boshlig'i","bosh."),("direktori","dir."),
                 ("boshqaruvchisi","boshqar."),("bo'limi","bo'lim")]:
        r = re.sub(r"\b" + re.escape(k) + r"\b", v, r, flags=re.IGNORECASE)

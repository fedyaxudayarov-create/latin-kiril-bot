def load_data():
    if os.path.exists(DATA_FILE):
        try:
            with open(DATA_FILE, "r", encoding="utf-8") as f:
                d     = json.load(f)
                staff = d.get("staff") or {}
                orgs  = d.get("orgs")  or {}
                # Yangi default kalitlarni qo'shamiz
                for k, v in DEFAULT_STAFF.items():
                    if k not in staff:
                        staff[k] = dict(v)
                for k, v in DEFAULT_ORGS.items():
                    if k not in orgs:
                        orgs[k] = [dict(i) for i in v]
                maktablar = d.get("maktablar") or [dict(m) for m in DEFAULT_MAKTABLAR]
                dmttlar   = d.get("dmttlar")   or [dict(d2) for d2 in DEFAULT_DMTTLAR]
                return staff, orgs, maktablar, dmttlar
        except Exception as e:
            print(f"data.json o'qishda xato: {e}")
    return ({k: dict(v) for k, v in DEFAULT_STAFF.items()},
            {k: [dict(i) for i in v] for k, v in DEFAULT_ORGS.items()},
            [dict(m) for m in DEFAULT_MAKTABLAR],
            [dict(d) for d in DEFAULT_DMTTLAR])

def save_data():
    with open(DATA_FILE, "w", encoding="utf-8") as f:
        json.dump({"staff": STAFF, "orgs": ORGS,
                   "maktablar": MAKTABLAR, "dmttlar": DMTTLAR},
                  f, ensure_ascii=False, indent=2)

